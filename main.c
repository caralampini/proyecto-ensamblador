int posicion_valida(int fila, int columna) {
  if (fila < 0 || fila >= FILAS) {
    return 0;
  }

  if (columna < 0 || columna >= COLUMNAS) {
    return 0;
  }

  return 1;
}

int inicio_ventana(int posicion) {
  int inicio = posicion - (VENTANA / 2);

  if (inicio < 0) {
    return 0;
  }

  if (inicio > FILAS - VENTANA) {
    return FILAS - VENTANA;
  }

  return inicio;
}

